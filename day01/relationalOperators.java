public class relationalOperators {
    public static void main(String[] args) {
        int x=10,y=20;
        System.out.println("  Relational Operators");      
        System.out.println(x<y);
        System.out.println(x<=y);
         System.out.println(x>y);
        System.out.println(x>=y);
        System.out.println(x==y);
        System.out.println(x!=y);
        
        System.out.println(" Logical Operators");
         System.out.println((x>y) && (x==y));
    }
}
